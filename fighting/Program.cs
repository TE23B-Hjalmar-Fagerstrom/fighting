static void spel()
{

    int candy = 10;
    
    Console.WriteLine("välkommen, för att starta och för att starta nästa runda trycker du på enter!");
    
    Console.ReadLine();

    while (candy >=0)
    {
    Console.WriteLine($"Du har {candy} och din kommpis vill satsa det på vem av din föräldrar som ");
    TheFighet();
    }

    Console.WriteLine("""


vill du spela igen? J/N 
""");
    string continu = Console.ReadLine();

    if (continu.ToLower() == "J")
    {
        spel();
    }
}

static void TheFighet()
{
    int hp1 = Random.Shared.Next(80, 101);
    int hp2 = Random.Shared.Next(80, 101);
    int round = 1;
    string figther1 = "Din pappa";
    string figther2 = "Din mamma"; 


    while (hp1 >= 0 && hp2 >= 0)
    {
        Console.WriteLine($"Runda {round}!");
        int dmg1 = Random.Shared.Next(5, 15);
        int dmg2 = Random.Shared.Next(5, 15);
        hp2 -= dmg1;
        hp1 -= dmg2;
        Console.WriteLine($"{figther1} slog {figther2} för {dmg1} och hon har {hp2} hp kvar");
        Console.WriteLine($"{figther2} slog din {figther1} för {dmg2} och han har {hp1} hp kvar");
        round++;
        Console.ReadLine();
    }


    if (hp1 <= 1 && hp2 >= 1)
    {
        Win2();
    }

    else if (hp2 <= 1 && hp1 >= 1)
    {
        Win1();
    }

    if (hp1 <= 0 && hp2 <= 0)
    {
        Draw();
    }
}

